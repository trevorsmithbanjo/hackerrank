'use strict';

const fs = require('fs');

process.stdin.resume();
process.stdin.setEncoding('utf-8');

let inputString = '';
let currentLine = 0;

process.stdin.on('data', inputStdin => {
    inputString += inputStdin;
});

process.stdin.on('end', _ => {
    inputString = inputString.replace(/\s*$/, '')
        .split('\n')
        .map(str => str.replace(/\s*$/, ''));

    main();
});

function readLine() {
    return inputString[currentLine++];
}

// Complete the sockMerchant function below.
function sockMerchant(n, ar) {
// Given an input int and arr retrun the amount of matched pairs in arr
// First sort the arr in ascending order
// Loop through arr equal to n times
// Create pairs var
// if arr[i] === arr[i + 1] pairs++ and i++ to move iterator up one
// return pairs

    let pairs = 0;
    ar.sort((a, b) => {return a - b});

    for (let i = 0; i < n; i++) {
        
        if (ar[i] === ar[i + 1]) {
            pairs++;
            i++
        }
}
console.log(pairs);
return pairs;
}

function main() {
    const ws = fs.createWriteStream(process.env.OUTPUT_PATH);

    const n = parseInt(readLine(), 10);

    const ar = readLine().split(' ').map(arTemp => parseInt(arTemp, 10));

    let result = sockMerchant(n, ar);

    ws.write(result + "\n");

    ws.end();
}
